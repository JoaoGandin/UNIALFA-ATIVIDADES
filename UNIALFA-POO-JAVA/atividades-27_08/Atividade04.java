

public class Atividade04 {

    public static void main(String[] args) {

        int i = 5;
        //pós incremento
        System.out.println("i++: ");
        System.out.println("O valor inicial de i: " + i);
        System.out.println("i++: " + i++);
        System.out.println("O valor de i após i++: " + i);
        System.out.println();
        //pré incremento
        System.out.println("++i: ");
        System.out.println("O valor inicial de i: " + i);
        System.out.println("++i: " + ++i);
        System.out.println("O valor de i após ++i: " + i);
        System.out.println();
        //pós decremento
        System.out.println("i--: ");
        System.out.println("O valor inicial de i: " + i);
        System.out.println("i--: " + i--);
        System.out.println("O valor de i após i--: " + i);
        System.out.println();
        //pré decremento
        System.out.println("--i: ");
        System.out.println("O valor inicial de i: " + i);
        System.out.println("--i: " + --i);
        System.out.println("O valor de i após --i: " + i);
        System.out.println();

        

    }
    
}
